public class DecompositionMain {
    public static void main(String[] args) {
       SequenceA();
       SequenceB();
       SequenceA();
       SequenceB();

        
    
    }
    public static void SequenceA(){
        System.out.println("  /\\" );
        System.out.println(" /  \\" );
        System.out.println("/    \\" );
    }
    public static void SequenceB(){
        System.out.println("\\    /" );
        System.out.println(" \\  /" );
        System.out.println("  \\/" );
    }

}